# Imprimimos un mensaje de bienvenida en la consola.
print("¡Hola, estudiante!")

# Pregunta al usuario que ingrese su nombre.
nombre = input("Por favor, ingresa tu nombre: ")

# Imprimimos un mensaje personalizado usando el nombre ingresado.
# Concatenamos (juntamos) texto usando el operador "+" para formar un mensaje personalizado.
print("Bienvenido, " + nombre + "! Este es tu primer programa en Python.")

# Preguntas y respuestas sobre el código:
#a. ¿Para qué sirven las líneas empiezan con "#" en Python?
# R: Las líneas que comienzan con "#" en Python son comentarios. Los comentarios son líneas de texto que el intérprete de Python ignora y no ejecuta. Sirven para agregar anotaciones y explicaciones al código, lo que facilita su comprensión y mantenimiento.

#b. ¿Para qué me sirve utilizar la función print()?
# R: La función print() se utiliza para mostrar texto o valores en la consola o terminal. Es una forma de generar salida visible para el usuario.

#c. ¿Qué guarda específicamente "nombre" en la línea 7?
# R: En la línea nombre = input("Por favor, ingresa tu nombre: "), la variable nombre almacena la cadena de texto que el usuario ingrese en la consola después de que se muestra el mensaje "Por favor, ingresa tu nombre:".

#d. ¿Qué realiza el símbolo ""?
# R: El símbolo "" (comillas dobles) se utiliza para definir una cadena de texto (string) en Python. Cualquier texto encerrado entre comillas dobles se considera una cadena de caracteres.

#e. ¿Qué significa concatenar?
# R: Concatenar significa unir o combinar dos o más cadenas de texto en una sola cadena.

#f. ¿Qué función cumple el símbolo +?
# R: En el contexto de las cadenas de texto, el símbolo + se utiliza para concatenar (unir) dos o más cadenas. Por ejemplo, "Hola " + "mundo" daría como resultado "Hola mundo".

